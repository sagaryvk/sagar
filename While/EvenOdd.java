public class EvenOdd
{
	public static void main(String[] args)
	{
		System.out.println("The First 10 Even Numbers are:");
		int i = 0;
		while(i<20)
		{
		System.out.println(i);
		i += 2;
		}
		
		int j = 1;
		System.out.println("The First 10 Odd Numbers are:");
		while(j<20)
		{
		System.out.println(j);
		j += 2;
		}
		
	}
}